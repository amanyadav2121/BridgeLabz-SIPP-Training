// Create a program to convert the distance of 10.8 kilometers to miles.
// Hint: 1 km = 0.621371 miles
// I/P => NONE
// O/P => The distance  ___ km in miles is ___


public class convertKmToMiles_3 {
    public static void main(String[] args) {
        double km=10.8;
        double miles=0.621371;
        double ans=miles*km;
    
        System.out.println("The distance 10.8 km in miles is  "+ans);
    }
   
}
